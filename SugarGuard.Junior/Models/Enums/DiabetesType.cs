// Тип диабета - влияет на рекомендации
namespace SugarGuard.Junior.Models.Enums;

public enum DiabetesType
{
    /// <summary>
    /// Диабет 1 типа - поджелудочная не вырабатывает инсулин
    /// Главное показание для этого приложения
    /// </summary>
    Type1,

    /// <summary>
    /// Диабет 2 типа - инсулинорезистентность
    /// </summary>
    Type2,

    /// <summary>
    /// LADA (латентный аутоиммунный диабет у взрослых)
    /// Медленно прогрессирующий диабет 1 типа
    /// </summary>
    LADA,

    /// <summary>
    /// Неоперационализированный/неизвестный тип
    /// </summary>
    Other
}
