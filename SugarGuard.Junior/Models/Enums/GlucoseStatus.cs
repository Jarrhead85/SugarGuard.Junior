// Статус глюкозы - используется для цветовой индикации
// Зеленый = норма, жёлтый = низко/высоко, красный = критически
namespace SugarGuard.Junior.Models.Enums;

public enum GlucoseStatus
{
    /// <summary>
    /// Критически низкий уровень (&lt; 3.3 ммоль/л)
    /// Требует немедленных действий
    /// </summary>
    CriticallyLow,

    /// <summary>
    /// Низкий уровень (3.3-3.9 ммоль/л)
    /// Нужна углеводная закуска
    /// </summary>
    Low,

    /// <summary>
    /// Нормальный диапазон (4.0-10.0 ммоль/л)
    /// Идеальное состояние
    /// </summary>
    Normal,

    /// <summary>
    /// Высокий уровень (10.1-15.0 ммоль/л)
    /// Может потребоваться инсулин
    /// </summary>
    High,

    /// <summary>
    /// Критически высокий уровень (&gt; 15.0 ммоль/л)
    /// Требует немедленных действий
    /// </summary>
    CriticallyHigh
}
