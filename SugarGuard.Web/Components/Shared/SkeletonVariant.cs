namespace SugarGuard.Web.Components.Shared;

/// <summary>
/// Визуальный пресет компонента
/// </summary>
public enum SkeletonVariant
{   
    Text, // Одна или несколько строк текста
   
    Heading, // Строка-заголовок 
   
    Avatar, // Аватар-круг или квадрат с опциональными строками справа
   
    Card, // Карточка с шапкой и телом из нескольких строк
   
    KpiCard, // KPI-карточка: метка + крупное значение + подпись
   
    Chart, // Область графика заданной высоты
    
    TableRow, // Строка таблицы с несколькими ячейками
   
    Custom // Произвольный контент
}
